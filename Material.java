package liquid;


public class Material
{
    public float m;
    public float rd;
    public float k;
    public float v;
    public float d;
    public float g;
    
    public Material(final float m, final float rd, final float k, final float v, final float d, final float g) {
        this.m = m;
        this.rd = rd;
        this.k = k;
        this.v = v;
        this.d = d;
        this.g = g;
    }
}
